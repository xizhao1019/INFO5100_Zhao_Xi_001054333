package Business.Restaurant;

/**
 *
 * @author zhaoxi
 */
public class Menu {
    private String dish;
    private double price;
    
    public String getDish() {
        return dish;
    }

    public void setDish(String dish) {
        this.dish = dish;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }
    
    @Override
    public String toString(){
        return dish;
    }
    
}
